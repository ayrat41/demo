package july22;

public class Intiges {

	public static void main(String[] args) {
		byte a=2;
		
	short b=-100;
	int c=3000000;
	long d=1000000000;
	float e=10.35f;
	double f=0.000001;
	
	System.out.println("a*b="+(a*b));
	System.out.println("c+d="+(c+d));
	System.out.println("e*d="+(e*d));
	System.out.println("f*b="+(f*b));
	
	
	

	}

}
